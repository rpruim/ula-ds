from figures import *

width=100
height = width
margin = 5

size = 4
beginfigure("subspace-xaxis", width+2*margin, height+2*margin)
setupcoordinates([margin, margin, width+margin, height+margin],
                 [-size, -size, size, size])

Grid([-size,1,size], [-size,1,size]).draw()

axes = Axes()
axes.draw()

Line([-size,0], [size,0], color=blue, linewidth=2).draw()

endfigure()
